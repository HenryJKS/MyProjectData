/* Overload ou Sobrecarga

Em uma procedure quando se tem parametros, somos obrigados a passar todos eles
Porém há casos que não tem necessidade, e não queremos passar alguns parametros
Podemos uar o Overload para quando não queremos passar um parametro na hora de executar uma procedure

Para fazer isso precisamos ir no cabeçalho do package e podemos copiar uma procedure com parametros, 
e tiramos os parametros que não queremos declarar 

Alteramos também o body do package, tirando todas manipulações que não vamos usar no parametro*/